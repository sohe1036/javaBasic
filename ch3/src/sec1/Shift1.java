package sec1;

public class Shift1 {

	public static void main(String[] args) {
		// 시프트(자리이동) 연산 ->비트 연산 (2진수연산)
		
		int a =20;
		System.out.println("a>>2 : "+(a>>2));
		System.out.println("a<<2 : "+(a<<2));
		System.out.println("a>>>2 : "+(a>>>2));		//논리연산자 
		//System.out.println("a<<<2 : "+(a<<<2));	//논리연산자는 자릿수 증가 x 
		
	}

}
